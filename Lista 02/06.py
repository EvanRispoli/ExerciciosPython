'''
Crie um algoritmo que leia um caractere e informe se o mesmo é uma vogal ou não.
'''

vogal = ['A', 'E','I','O','U','a', 'e', 'i', 'o', 'u']
caracter = input('Digite um caracter: ')
if caracter in vogal :
    print('O caracter digitado e uma vogal')
else:
    print('O caracter digitado nao e uma vogal')




