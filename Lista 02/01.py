'''
Crie um algoritmo que leia um número e mostre se o mesmo é positivo, negativo ou zero.
'''
num = float(input('Informe um numero: '))
if num > 0:
    print('o numero informado e positivo')

elif num == 0:
    print('O valor informado e igual a 0')

else:
    print('O valor informado e negativo')
